"""
pyrit_mcp.tools.results — Result storage and reporting tools (Domain 5).

These tools query, filter, and export red-team findings from completed attack
campaigns. The pyrit_generate_report tool returns structured JSON data that
Claude narrates into a vulnerability report — the report prose is always
written by Claude, not generated by an LLM inside the server.

Tools exposed:
  - pyrit_list_attacks
  - pyrit_get_attack_results
  - pyrit_get_successful_jailbreaks
  - pyrit_get_failed_attacks
  - pyrit_export_results_csv
  - pyrit_generate_report
  - pyrit_compare_attacks
  - pyrit_clear_session
"""

from __future__ import annotations

import csv
import json
import logging
from pathlib import Path
from typing import Any

from pyrit_mcp.utils.db import execute, fetchall, fetchone
from pyrit_mcp.utils.formatters import error, pending, success

log = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _row_to_result(row: tuple[Any, ...]) -> dict[str, Any]:
    """Convert a results table row to a public dict."""
    result_id, attack_id, prompt_text, response_text, scores_raw, timestamp = row
    scores = json.loads(scores_raw) if isinstance(scores_raw, str) and scores_raw else {}
    return {
        "result_id": str(result_id),
        "attack_id": str(attack_id),
        "prompt_text": prompt_text,
        "response_text": response_text or "",
        "scores": scores,
        "timestamp": str(timestamp),
    }


def _row_to_attack(row: tuple[Any, ...]) -> dict[str, Any]:
    """Convert an attacks table row to a public dict."""
    (
        attack_id,
        target_id,
        dataset_name,
        orch_type,
        status,
        started_at,
        completed_at,
        metadata_raw,
    ) = row
    metadata = json.loads(metadata_raw) if isinstance(metadata_raw, str) and metadata_raw else {}
    return {
        "attack_id": str(attack_id),
        "target_id": str(target_id) if target_id else None,
        "dataset_name": dataset_name,
        "orchestrator_type": orch_type,
        "status": status,
        "started_at": str(started_at) if started_at else None,
        "completed_at": str(completed_at) if completed_at else None,
        "metadata": metadata,
    }


